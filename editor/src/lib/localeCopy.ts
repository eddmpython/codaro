let activeLocale: AppLocale = detectBrowserLocale();

export function detectBrowserLocale(): AppLocale {
  if (typeof navigator === "undefined") return "ko";
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return languages.some((language) => String(language).toLowerCase().startsWith("en")) ? "en" : "ko";
}

export function getActiveLocale(): AppLocale {
  return activeLocale;
}

export function setActiveLocale(locale: AppLocale): void {
  activeLocale = locale;
}

export function translate(key: string, values?: Record<string, string | number | null | undefined>): string {
  return translateWithLocale(activeLocale, key, values);
}

export function translateWithLocale(
  locale: AppLocale,
  key: string,
  values?: Record<string, string | number | null | undefined>,
): string {
  const template = messages[locale][key] ?? messages.ko[key] ?? key;
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? ""));
}

export function nextLocale(locale: AppLocale): AppLocale {
  return locale === "en" ? "ko" : "en";
}

export function localeDateFormat(locale: AppLocale): string {
  return locale === "en" ? "en-US" : "ko-KR";
}

export function aiLanguageInstruction(locale: AppLocale): string {
  if (locale === "en") {
    return [
      "User-facing answer language: English.",
      "Preserve code, identifiers, package names, file paths, API names, traceback text, stdout, stderr, and tool payloads exactly unless the user explicitly asks to translate them.",
      "If the user writes Korean while the display locale is English, answer in English unless they explicitly request Korean.",
    ].join(" ");
  }
  return [
    "User-facing answer language: Korean.",
    "Preserve code, identifiers, package names, file paths, API names, traceback text, stdout, stderr, and tool payloads exactly unless the user explicitly asks to translate them.",
  ].join(" ");
}
