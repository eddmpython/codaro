export function isPythonStdlibModule(moduleName: string) {
  const normalized = moduleName.trim().split(".")[0] ?? "";
  return normalized !== "" && PYTHON_STDLIB_MODULES.has(normalized);
}

export function isPythonStdlibPackageName(packageName: string) {
  const baseName = packageName.trim().match(/^[A-Za-z0-9_.-]+/)?.[0] ?? "";
  return baseName !== "" && isPythonStdlibModule(baseName.replace(/-/g, "_"));
}
